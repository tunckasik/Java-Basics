
public class KO_Main {
    public static void main(String[] args) {
       /* 
        Karsilastirma Operatorleri
        
        == ---> Esit mi?
        != ---> Esit Degil mi?
        >  ---> Buyuk mu?
        >= ---> Buyuk veya Esit mi?
        <  ---> Kucuk mu ?
        <= ---> Kucuk veya Esit mi?
        
        
        */
        
        System.out.println(3 == 3);
        System.out.println(3 != 4);
        System.out.println(2>1);
        System.out.println(2>=3);
        System.out.println(2<1);
        System.out.println(2<=1);
        
        System.out.println("Mehmet" == "Mehmet"); //string leri de karsilastirabiliyoruz.
    }
}
